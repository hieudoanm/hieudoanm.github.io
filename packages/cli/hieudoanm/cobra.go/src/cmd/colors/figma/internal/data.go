func Lookup(name string) (string, bool) {
	for k, v := range figmaColors {
		if strings.EqualFold(k, name) {
			return v, true
		}
	}
	return "", false
}

func All() map[string]string {
	return figmaColors
}

func FindExactName(query string) string {
	for n := range figmaColors {
		if strings.EqualFold(n, query) {
			return n
		}
	}
	return query
}

func Palettes() map[string][]PaletteColor {
	return figmaPalettes
}

func PaletteNames() []string {
	return figmaPaletteNames
}

func FindPalettesByHex(hex string) []string {
	hex = strings.ToUpper(hex)
	if !strings.HasPrefix(hex, "#") {
		hex = "#" + hex
	}
	var result []string
	for pn, colors := range figmaPalettes {
		for _, c := range colors {
			if strings.EqualFold(c.Hex, hex) {
				result = append(result, pn)
				break
			}
		}
	}
	return result
}
