const FONTS: Record<string, FigletFont> = {
  Standard: STANDARD,
  Block: BLOCK,
  Small: SMALL,
  Banner: BANNER,
};

const renderFiglet = (text: string, fontName: string): string => {
  const font = FONTS[fontName] ?? FONTS.Standard;
  const chars = text.toUpperCase().split('');
  const fallback = font[' '] ?? ['   ', '   ', '   ', '   ', '   '];
  const rows = fallback.length;

  const lines: string[] = Array(rows).fill('');
  for (const ch of chars) {
    const glyph = font[ch] ?? font['?'] ?? fallback;
    const h = glyph.length;
    for (let r = 0; r < rows; r++) {
      lines[r] += r < h ? glyph[r] : ' '.repeat((glyph[0] ?? '').length);
    }
  }
  return lines.join('\n');
};

const FONT_NAMES = Object.keys(FONTS);

export const FigletModal = (props: { onClose: () => void }) => {
  const [text, setText] = createSignal('Hello, World!');
  const [font, setFont] = createSignal('Standard');
  const [copied, setCopied] = createSignal(false);

  const output = () => renderFiglet(text() || 'Hello, World!', font());

  const copy = async () => {
    await navigator.clipboard.writeText(output());
    setCopied(true);
    setTimeout(() => setCopied(false), 2000);
  };

  const onKeyDown = (e: KeyboardEvent) => {
    if (e.key === 'Escape') props.onClose();
  };

  return (
    <ModalWrapper
      onClose={props.onClose}
      title="FIGlet Generator"
      size="max-w-2xl">
      <div onKeyDown={onKeyDown}>
        {/* Input */}
        <div class="mb-3 flex gap-2">
          <input
            autoFocus
            type="text"
            value={text()}
            onChange={(e: Event) =>
              setText((e.target as HTMLInputElement).value)
            }
            placeholder="Type something…"
            maxLength={20}
            class="input input-bordered flex-1 font-mono"
          />
          <select
            value={font()}
            onChange={(e: Event) =>
              setFont((e.target as HTMLSelectElement).value)
            }
            class="select select-bordered">
            {FONT_NAMES.map((f) => (
              <option key={f} value={f}>
                {f}
              </option>
            ))}
          </select>
        </div>

        {/* Font tabs */}
        <div role="tablist" class="tabs tabs-boxed mb-4">
          {FONT_NAMES.map((f) => (
            <a
              key={f}
              role="tab"
              class={`tab ${font() === f ? 'tab-active' : ''}`}
              onClick={() => setFont(f)}>
              {f}
            </a>
          ))}
        </div>

        {/* Output */}
        <div class="bg-base-200 mb-3 overflow-x-auto rounded-xl p-4">
          <pre class="text-base-content/80 font-mono text-xs leading-tight whitespace-pre">
            {output()}
          </pre>
        </div>

        {/* Character count + hint */}
        <div class="mb-3 flex items-center justify-between text-xs opacity-40">
          <span>
            {text().length}/20 chars · letters, digits, punctuation supported
          </span>
          <span>Rendered in {font()}</span>
        </div>

        {/* Actions */}
        <div class="flex gap-2">
          <button
            class={`btn btn-sm flex-1 ${copied() ? 'btn-success' : 'btn-primary'}`}
            onClick={copy}>
            {copied() ? '✓ Copied' : '📋 Copy ASCII'}
          </button>
          <button class="btn btn-ghost btn-sm" onClick={() => setText('')}>
            Clear
          </button>
        </div>
      </div>
    </ModalWrapper>
  );
};
