# Ensure stdlib math is loaded before libs/math shadows it
_libs_path = os.path.dirname(os.path.abspath(__file__))
_entries_to_restore = [p for p in sys.path if os.path.abspath(p) == _libs_path]
for p in _entries_to_restore:
    sys.path.remove(p)

import math as _stdlib_math
sys.modules["math"] = _stdlib_math

for p in reversed(_entries_to_restore):
    sys.path.insert(0, p)

